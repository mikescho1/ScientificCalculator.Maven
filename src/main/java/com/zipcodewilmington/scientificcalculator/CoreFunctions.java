package com.zipcodewilmington.scientificcalculator;

public class CoreFunctions {


    public double add(double num1, double num2) {
        return /*this.answer =*/ num1 + num2;
    }

    public double subtract(double num1, double num2)    {
        return /*this.answer =*/ num1 - num2;
    }

    public double multiply(double num1, double num2)    {
        return  /*this.answer =*/ num1 * num2;
    }

    public double divide(double num1, double num2)  {
        return  /*this.answer =*/ num1 / num2;
    }

    public double power(double num1, double num2) {
        return /*this.answer =*/ Math.pow(num1, num2);
    }

    public double square(double num1)   {
        return /*this.answer =*/ Math.pow(num1, 2);
    }

    public double squareRt(double num1)    {
        return /*this.answer =*/ Math.sqrt(num1);
    }

    public double invertSign(double num1)  {
        return /*this.answer =*/ num1 / -1;
    }

    public double inverse(double num1) {
        return /*this.answer =*/ num1 / -1;
    }







}
